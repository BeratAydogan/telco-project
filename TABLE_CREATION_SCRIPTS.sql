
CREATE TABLE Tariffs(
	TARIFF_ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	TARIFF_NAME NVARCHAR2(50) NOT NULL UNIQUE,
	MONTHLY_FEE NUMBER(10,2) NOT NULL,
	DATA_LIMIT INT NOT NULL,
	MINUTE_LIMIT INT NOT NULL,
	SMS_LIMIT INT NOT NULL,
	
	CONSTRAINT CHK_Fee_min CHECK (MONTHLY_FEE >=0),
	CONSTRAINT CHK_Data_min CHECK (DATA_LIMIT  >=0),
	CONSTRAINT CHK_Minute_min CHECK (MINUTE_LIMIT  >=0),
	CONSTRAINT CHK_Sms_min CHECK (SMS_LIMIT  >=0)
	
	
);


CREATE TABLE Customers (
	CUSTOMER_ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	CUSTOMER_NAME NVARCHAR2(100) NOT NULL,
	CITY NVARCHAR2(30) NOT NULL,
	SIGNUP_DATE DATE DEFAULT SYSDATE,
	TARIFF_ID INT NOT NULL,
	
	CONSTRAINT FK_Tariff_Link FOREIGN KEY (TARIFF_ID) 
        REFERENCES Tariffs(TARIFF_ID)	
);


CREATE TABLE Monthly_Stats(
	ID INT PRIMARY KEY,
	CUSTOMER_ID INT NOT NULL,
	DATA_USAGE NUMBER(10,2) DEFAULT 0 NOT NULL ,
	MINUTE_USAGE INT DEFAULT 0 NOT NULL ,
	SMS_USAGE INT DEFAULT 0 NOT NULL ,
	PAYMENT_STATUS NVARCHAR2(10) DEFAULT 'UNPAID' NOT NULL ,
	
	CONSTRAINT FK_ID_Match FOREIGN KEY (ID) 
		REFERENCES Customers(CUSTOMER_ID) 
		ON DELETE CASCADE,
	CONSTRAINT CHK_Datas CHECK (DATA_USAGE>=0 AND MINUTE_USAGE>=0 AND SMS_USAGE>=0 )
	
	
);